create table public._user
(
    id       bigint not null
        primary key,
    email    varchar(255),
    name     varchar(255),
    password varchar(255),
    role     varchar(255)
        constraint _user_role_check
            check ((role)::text = 'USER'::text)
    );

alter table public._user
    owner to postgres;

